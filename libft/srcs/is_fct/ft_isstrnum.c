#include <libft.h>

int	ft_isstrnum(char *s)
{
	int	i;

	i = 0;
	if (s && s[i] && s[i] == '-' && s[i + 1])
		i++;
	while (s && s[i])
	{
		if (!ft_isnum(s[i]))
			return (0);
		i++;
	}
	return (1);
}
