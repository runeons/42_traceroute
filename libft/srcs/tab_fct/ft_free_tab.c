#include <libft.h>

int	ft_free_tab(char ***t)
{
	int	i;

	i = 0;
	while ((*t) && (*t)[i])
	{
		m_free((*t)[i]);
		(*t)[i] = NULL;
		i++;
	}
	m_free(*t);
	(*t) = NULL;
	return (0);
}
